def minimum_of_two(arr_1, arr_2)
    mins_arr = []
    arr_1.each_with_index do |num_1, idx_1|
        arr_2.each_with_index do |num_2, idx_2|
            if idx_1 == idx_2
                num_1 <= num_2 ? mins_arr.push(num_1) : mins_arr.push(num_2)
            end
        end
    end
    mins_arr
end


